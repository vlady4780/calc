function resta1(num1, num2) {
    this.num1 = num1;
    this.num2 = num2
    return num1 - num2;
}

function suma1(num1, num2) {
    this.num1 = num1;
    this.num2 = num2
    return num1 + num2;
}

function multiplicacion1(num1, num2) {
    this.num1 = num1;
    this.num2 = num2
    return num1 * num2;
}

function division1(num1, num2) {
    this.num1 = num1;
    this.num2 = num2
    return num1 / num2;
}